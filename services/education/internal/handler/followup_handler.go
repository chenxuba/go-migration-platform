func (handler *Handler) followUpCount(w http.ResponseWriter, r *http.Request) {
	ctx := tenant.FromContext(r.Context())
	claims, ok := handler.requireAuth(w, r, ctx)
	if !ok {
		return
	}
	var dto model.FollowUpCountDTO
	if raw := strings.TrimSpace(r.URL.Query().Get("queryAllOrDepartment")); raw != "" {
		if value, err := strconv.Atoi(raw); err == nil {
			dto.QueryAllOrDepartment = &value
		}
	}
	if raw := strings.TrimSpace(r.URL.Query().Get("deptId")); raw != "" {
		if value, err := strconv.ParseInt(raw, 10, 64); err == nil {
			dto.DeptID = &value
		}
	}
	result, err := handler.service.GetFollowUpCount(claims.UserID, dto)
	if err != nil {
		httpx.WriteError(w, http.StatusBadRequest, err.Error(), ctx.RequestID)
		return
	}
	httpx.WriteJSON(w, http.StatusOK, result, ctx.RequestID)
}

